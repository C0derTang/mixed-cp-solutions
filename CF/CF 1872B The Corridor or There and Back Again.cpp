const int dulo = 998244353;

int binexp(int a, int b){
    int ret = 1;
    while (b>0){
        if (b&1) ret *= a;
        a *= a;
        b >>= 1;
    }
    return ret;
}

int modinv(int a){
    return binexp(a, dulo-2);
}

bool cbs(pair<int, int> a, pair<int, int> b) {
    return a.second < b.second;
}


void solve(){
    int n; cin >> n;
    vector<pair<int, int>> traps(n);
    for(int i=0; i<n; ++i) cin >> traps[i].first >> traps[i].second;

    sort(traps.begin(), traps.end());
    int ans = 400;
    for(auto x: traps){
        ans = min(ans, x.first + (x.second-1)/2);
    }
    cout << ans << '\n';
}

